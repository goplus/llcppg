func (p *Package) decl(usr string, name string, doc *ast.CommentGroup) (*gogen.TypeDecl, *gogen.TypeDefs) {
	decl, ok := p.decls[usr]
	if !ok {
		panic("emptyTypeDecl2: decl not found")
	}
	decl.SetComments(CommentGroup(doc).CommentGroup)
	return decl.NewType(name), decl
}

func (p *Package) registerDefine(usr string, obj types.Object, cname string, pubname string, node ast.Node) {
	typeObj := obj
	var err error
	if cname != pubname {
		// if have the same name,substitute the object
		typeObj, err = substObj(p.p.Types, p.p.Types.Scope(), cname, obj)
		if err != nil {
			log.Panic(err)
		}
	}
	p.defines[usr] = node
	p.from[typeObj] = node
}

// handleTypeDecl creates a new type declaration or retrieves existing one
func (p *Package) handleTypeDecl(pubname string, cname string, typeDecl *ast.TypeDecl) *Incomplete {
	if existDecl, exists := p.incompleteTypes.Lookup(cname); exists {
		return existDecl
	}
	decl := p.emptyTypeDecl(pubname, typeDecl.Doc)
	inc := &Incomplete{
		cname: cname,
		file:  p.curFile,
		decl:  decl,
		getType: func() (types.Type, error) {
			return types.NewStruct(p.cvt.defaultRecordField(), nil), nil
		},
	}
	p.incompleteTypes.Add(inc)
	return inc
}

func (p *Package) handleTypeDecl2(pubname string, cname string, typeDecl *ast.TypeDecl) *Incomplete {
	if existDecl, exists := p.incompleteTypes.Lookup(cname); exists {
		return existDecl
	}
	decl, _ := p.decl(typeDecl.Name.USR, pubname, typeDecl.Doc)
	inc := &Incomplete{
		cname: cname,
		file:  p.curFile,
		decl:  decl,
		getType: func() (types.Type, error) {
			return types.NewStruct(p.cvt.defaultRecordField(), nil), nil
		},
	}
	p.incompleteTypes.Add(inc)
	return inc
}

func (p *Package) handleCompleteType(incom *Incomplete, typ *ast.RecordType, name string) error {
	defer p.incompleteTypes.Complete(name)
	defer p.SetCurFile(p.curFile)
	p.SetCurFile(incom.file)
	structType, err := p.cvt.RecordTypeToStruct(typ)
	if err != nil {
		// For incomplete type's conerter error, we use default struct type
		incom.decl.InitType(p.p, types.NewStruct(p.cvt.defaultRecordField(), nil))
		return err
	}
	incom.decl.InitType(p.p, structType)
	return nil
}

// handleImplicitForwardDecl handles type references that cannot be found in the current scope.
// For such declarations, create a empty type decl and store it in the
// incomplete map, but not in the public symbol table.
func (p *Package) handleImplicitForwardDecl(ident *ast.Ident) *gogen.TypeDecl {
	if decl, ok := p.incompleteTypes.Lookup(ident.Name); ok {
		return decl.decl
	}

	pubName := p.nameMapper.GetGoName(ident.Name, p.trimPrefixes())
	decl := p.emptyTypeDecl(pubName, nil)
	inc := &Incomplete{
		cname: ident.Name,
		file:  p.curFile,
		decl:  decl,
		getType: func() (types.Type, error) {
			return types.NewStruct(p.cvt.defaultRecordField(), nil), nil
		},
	}

	p.incompleteTypes.Add(inc)
	p.nameMapper.SetMapping(ident.Name, pubName)
	p.registerDefine(ident.USR, decl.Type().Obj(), ident.Name, pubName, nil)
	return decl
}

func (p *Package) emptyTypeDecl(name string, doc *ast.CommentGroup) *gogen.TypeDecl {
	typeBlock := p.p.NewTypeDefs()
	typeBlock.SetComments(CommentGroup(doc).CommentGroup)
	return typeBlock.NewType(name)
}

func (p *Package) NewTypedefDecl(typedefDecl *ast.TypedefDecl) error {
	skip, _, err := p.cvt.handleSysType(typedefDecl.Name, typedefDecl.Loc, p.curFile.IncPath)
	if skip {
		if dbg.GetDebugLog() {
			log.Printf("NewTypedefDecl: %v is a typedef of system header file\n", typedefDecl.Name)
		}
		return err
	}
	if dbg.GetDebugLog() {
		log.Printf("NewTypedefDecl: %v\n", typedefDecl.Name)
	}

	sameName, err := p.allowTypedef(typedefDecl.Name.Name)
	if err != nil {
		// same typedef , throw error
		return err
	}
	if sameName {
		// same name,not same definition,permit
		// like struct A {};
		// typedef struct A {};
		return nil
	}

	name, _, err := p.DeclName(typedefDecl.Name.Name)
	if err != nil {
		return err
	}

	p.CollectNameMapping(typedefDecl.Name.Name, name)

	genDecl := p.p.NewTypeDefs()
	typeSpecdecl := genDecl.NewType(name)

	p.registerDefine(typedefDecl.Name.USR, typeSpecdecl.Type().Obj(), typedefDecl.Name.Name, name, typedefDecl)

	deferInit := p.handleTyperefIncomplete(typedefDecl.Type, typeSpecdecl, typedefDecl.Name.Name)
	if deferInit {
		if dbg.GetDebugLog() {
			log.Printf("NewTypedefDecl: %s defer init\n", name)
		}
		return nil
	}

	typ, err := p.ToType(typedefDecl.Type)
	if err != nil {
		typeSpecdecl.InitType(p.p, types.NewStruct(p.cvt.defaultRecordField(), nil))
		return err
	}

	typeSpecdecl.InitType(p.p, typ)
	if _, ok := typ.(*types.Signature); ok {
		genDecl.SetComments(NewTypecDocComments())
	}

	return nil
}

